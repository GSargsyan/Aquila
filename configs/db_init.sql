CREATE TABLE levels (
	id SMALLINT PRIMARY KEY,
	exp_thru INT
);

CREATE TABLE countries (
	name TEXT,
	iso_2 CHAR(2) PRIMARY KEY,
	iso_3 CHAR(3),
	iso_num CHAR(3)
);

CREATE TABLE rooms (
	id SERIAL PRIMARY KEY,
	player_id_list INTEGER ARRAY,
	is_running BOOLEAN
);

CREATE TYPE PLAYER_STATUS AS ENUM ('alive', 'suspended');

CREATE TABLE players (
	id SERIAL PRIMARY KEY,
	username TEXT,
	password TEXT,
	balance BIGINT,
	demo_balance INT,
	level_id SMALLINT REFERENCES levels,
	exp INT,
	wagered BIGINT,
	won BIGINT,
	lost BIGINT,
	status PLAYER_STATUS,
	chat_messages_count INT,
	bets_count INT,
	bets_won_count INT,
	country_id CHAR(2) REFERENCES countries,
	registered_date TIMESTAMP,
	settings JSON,
	room_id SMALLINT REFERENCES rooms,
	last_checkup TIMESTAMP,
	is_online BOOLEAN
);

CREATE TABLE player_blocks (
	id SERIAL PRIMARY KEY,
	player_id INT REFERENCES players,
	block_date TIMESTAMP,
	unblock_date TIMESTAMP,
	reason SMALLINT
);

CREATE TYPE RELATION AS ENUM ('muted', 'voice_muted');

CREATE TABLE player_relations (
	player_id INT REFERENCES players,
	ref_player_id INT REFERENCES players,
	relation RELATION
);

CREATE TABLE feedbacks (
	id SERIAL PRIMARY KEY,
	player_id INT REFERENCES players,
	content TEXT,
	date TIMESTAMP,
	answered TIMESTAMP
);

CREATE TYPE ACTION AS ENUM ('login', 'logout');

CREATE TABLE login_log (
	id SERIAL PRIMARY KEY,
	player_id INT REFERENCES players,
	action ACTION,
	ip CIDR,
	date TIMESTAMP
);

CREATE TABLE rounds (
	id SERIAL PRIMARY KEY,
	room_id INT REFERENCES rooms,
	player_id_list INTEGER ARRAY,
	outcome SMALLINT,
	start_date TIMESTAMP,
	end_date TIMESTAMP
);

CREATE TABLE chat_messages (
	id BIGSERIAL PRIMARY KEY,
	sender_id INT REFERENCES players,
	room_id INT REFERENCES rooms,
	content TEXT,
	date TIMESTAMP
);

CREATE TABLE room_log (
	id SERIAL PRIMARY KEY,
	room_id INT REFERENCES rooms, 
	player_id INT REFERENCES players,
	entry_date TIMESTAMP,
	leave_date TIMESTAMP
);

CREATE TABLE bet_types (
	id TEXT PRIMARY KEY,
	winning_vals INTEGER ARRAY,
	payout SMALLINT
);

CREATE TABLE bets (
	id BIGSERIAL PRIMARY KEY,
	player_id INT REFERENCES players,
	round_id INT REFERENCES rounds,
	bet_on TEXT REFERENCES bet_types,
	amount BIGINT,
	is_real BOOLEAN
);

CREATE TABLE ip_blocks (
	id SERIAL PRIMARY KEY,
	ip CIDR,
	block_date TIMESTAMP,
	unblock_date TIMESTAMP,
	reason SMALLINT
);

